from numpy import array
# creating array
data = array([
    [11, 22],
    [33,44],
    [55,66]
])

# printing array by first index only
print(data[0])
print("\n")

# printing array by first with comma index only
print(data[0,])
print("\n")

# printing array by first index and negative indexing only
print(data[-1])
print("\n")

# printing array by first index and negative indexing only
print(data[-1,])