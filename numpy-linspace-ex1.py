from numpy import linspace
# creating linspace an step spaces based array
# step size = stop-start/range-1
# step size = 10-1/3-1 
#           = 4.50
a1 = linspace(1,10,3)
print(a1)

# step size = stop-start/range-1
# step size = 10-1/5-1 
#           = 2.25
a2 = linspace(1,10,5)
print(a2)

# step size = stop-start/range-1
# step size = 1-0/10-1 
#           = 0.11
a3 = linspace(0,1,10)
print(a3)